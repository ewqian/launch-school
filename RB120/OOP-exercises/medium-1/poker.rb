class PokerHand

  def initialize(deck)
    @cards = deck.instance_of?(Deck) ? deck.deck[0..4] : deck[0..4]
  end

  def print
    puts cards
  end

  def evaluate
    case
    when royal_flush?     then 'Royal flush'
    when straight_flush?  then 'Straight flush'
    when four_of_a_kind?  then 'Four of a kind'
    when full_house?      then 'Full house'
    when flush?           then 'Flush'
    when straight?        then 'Straight'
    when three_of_a_kind? then 'Three of a kind'
    when two_pair?        then 'Two pair'
    when pair?            then 'Pair'
    else                       'High card'
    end
  end

  private

  attr_reader :cards

  # A, K, Q, J, 10 all of the same suit
  def royal_flush?
    flush?() && (cards.map { |card| card.value }.sort == [10, 11, 12, 13, 14])
  end

  # Five cards of the same suit in sequence
  def straight_flush?
    flush?() && straight?()
  end

  # Four cards of the same rank
  def four_of_a_kind?
    hand_ranks.each { |rank| return true if hand_ranks.count(rank) == 4 }

    false
  end

  # Three cards of one rank and two of another
  def full_house?
    rank_count = hand_ranks.uniq.map { |rank| hand_ranks.count(rank) }.sort
    rank_count.first == 2 && rank_count.last == 3
  end

  # Five cards of the same suit
  def flush?
    cards.map { |card| card.suit }.uniq.count == 1
  end

  # Five cards in sequence (for example, 4, 5, 6, 7, 8)
  def straight?
    sorted_vals = cards.map { |card| card.value }.sort
    incr_seq = (sorted_vals.first .. (sorted_vals.first + 4)).to_a
    decr_seq = (sorted_vals.last).downto(sorted_vals.last - 4).to_a

    sorted_vals == incr_seq || sorted_vals == decr_seq
  end

  # Three cards of the same rank
  def three_of_a_kind?
    hand_ranks.each { |rank| return true if hand_ranks.count(rank) == 3 }

    false
  end

  # Two cards of one rank and two cards of another
  def two_pair?
    uniq_ranks = hand_ranks.uniq
    num_pairs = uniq_ranks.count { |rank| hand_ranks.count(rank) == 2 }

    num_pairs == 2
  end

  # Two cards of the same rank
  def pair?
    hand_ranks.each { |rank| return true if hand_ranks.count(rank) == 2 }

    false
  end

  def hand_ranks
    cards.map { |card| card.rank }
  end
end

class Deck
  RANKS = ((2..10).to_a + %w(Jack Queen King Ace)).freeze
  SUITS = %w(Hearts Clubs Diamonds Spades).freeze

  attr_reader :deck

  def initialize
    reset
  end

  def draw
    reset if @deck.empty?
    @deck.pop
  end

  private

  def reset
    @deck = RANKS.product(SUITS).map do |rank, suit|
      Card.new(rank, suit)
    end

    @deck.shuffle!
  end
end

class Card
  include Comparable
  attr_reader :rank, :suit

  VALUES = { 'Jack' => 11, 'Queen' => 12, 'King' => 13, 'Ace' => 14 }

  def initialize(rank, suit)
    @rank = rank
    @suit = suit
  end

  def to_s
    "#{rank} of #{suit}"
  end

  def value
    VALUES.fetch(rank, rank)
  end

  def <=>(other_card)
    value <=> other_card.value
  end
end

